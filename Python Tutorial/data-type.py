# Setting the data type
x = "hello world" # str
x = 20 # int
x = 20.5 # float
x = 20j # complex
x = ['aple', 'banana', 'cherry'] # list
x = ('apple', 'banana', 'cherry') # tuple
x = range(6) # range
x = {'name': 'sakti', 'age': 19} # dict
x = {'aple', 'banana', 'cherry'} # set
x = frozenset({'aple', 'banana', 'cherry'}) #frozenset
x = True # bool
x = b'hello world' # bytes
x = bytearray(5) # bytes array
x = memoryview(bytes(5)) # memoryview
x = None # NoneType